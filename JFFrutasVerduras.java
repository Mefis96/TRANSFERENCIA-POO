    private void btnCrearFMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearFMouseClicked
        String nombreFruta = txtnombreF.getText();
        String colorFruta = txtcolorF.getText();
        int caloriasFruta = Integer.parseInt(txtCaloriasF.getText());
        if (caloriasFruta >100 ){
            JOptionPane.showMessageDialog(this, "Error, este alimento no es una fruta, verifique");
            return;
        }
        int pesoFruta = Integer.parseInt(txtpesoF.getText());
        boolean esFruta = CBFruta.isSelected();
        
        FrutasVerduras fruta1 = new FrutasVerduras (nombreFruta, colorFruta, caloriasFruta, pesoFruta, esFruta);
        JOptionPane.showMessageDialog(this, fruta1);
        listaAlim.add(fruta1);
        this.setVisible(false);
       
        
    }//GEN-LAST:event_btnCrearFMouseClicked

